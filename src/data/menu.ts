function createMenuItem(
  item: Pick<
    MenuItem,
    "id" | "categoryId" | "name" | "description" | "weight" | "price"
  >,
): MenuItem {
  const imageSrc = menuImageByItemId[item.id as keyof typeof menuImageByItemId];

  if (!imageSrc) {
    throw new Error(`Menu image is missing for item: ${item.id}`);
  }

  return {
    ...item,
    image: {
      src: publicPath(imageSrc),
      alt: `Фотография позиции «${item.name}»`,
      width: 960,
      height: 720,
    },
    available: true,
    badges: [],
    options: [],
    allowComment: true,
    isTemporaryData: false,
  };
}

